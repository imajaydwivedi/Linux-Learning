:<<'COMMENTS'

-> Groups are stored in /etc/group
-> Common properties are:
    - Group Name
    - Password Placeholder
    - Group ID (GID)
    - Group Members

-> Users that are a group member as a secondary group are also in /etc/group
-> Notice that primary group membership is uniquely administrated through /etc/passwd
-> Groups do not have significant other properties

-> Use "vigr" to edit /etc/group
COMMENTS
